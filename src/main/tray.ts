        0x00, 0x04, 0x67, 0x41, 0x4D, 0x41, 0x00, 0x00, 0xB1, 0x8F, 0x0B, 0xFC,
        0x61, 0x05, 0x00, 0x00, 0x00, 0x09, 0x70, 0x48, 0x59, 0x73, 0x00, 0x00,
        0x0E, 0xC3, 0x00, 0x00, 0x0E, 0xC3, 0x01, 0xC7, 0x6F, 0xA8, 0x64, 0x00,
        0x00, 0x00, 0x2F, 0x49, 0x44, 0x41, 0x54, 0x38, 0x4F, 0x63, 0xF8, 0xFF,
        0xFF, 0x3F, 0x03, 0x25, 0x80, 0x89, 0x81, 0x4D, 0x18, 0x19, 0x00, 0x69,
        0x67, 0x60, 0x64, 0xC0, 0x36, 0x68, 0x88, 0x18, 0x00, 0x69, 0x67, 0x60,
        0x64, 0xC0, 0x36, 0x68, 0x88, 0x18, 0x00, 0x69, 0x67, 0x60, 0x64, 0xC0,
        0x36, 0x68, 0x88, 0x18, 0x00, 0x69, 0x67, 0x60, 0x64, 0xC0, 0x36, 0x00,
